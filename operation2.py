#Class to sommme two pair of num
class Somme:

    def somar():
        a1 = float(input("Insert a number: "))
        a2 = float(input("Insert another number: "))
        print(f"answer: {a1+a2}")

#Class to substract two pair of num
class Soustraire:

    def subtrair():
        a1 = float(input("Insert a number: "))
        a2 = float(input("Insert another number: "))
        print(f"answer: {a1-a2}")